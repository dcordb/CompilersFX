package syntaxAnalyzers.entries;

public abstract class TableEntry {
    protected String text;

    public TableEntry(String text) {
        this.text = text;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    @Override
    public String toString() {
        return text;
    }
}